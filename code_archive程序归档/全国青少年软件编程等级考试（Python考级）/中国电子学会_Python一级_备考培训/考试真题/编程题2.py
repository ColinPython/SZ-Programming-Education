# 请使用 input() 输入圆的半径 r
pi = 3.14
r = float(input("请输入半径："))
a = 2 * r
area = pi * r * r  # area 美 /ˈeriə/  n.面积
perimeter = 2 * pi * r  # perimeter 美 /pəˈrɪmɪtər/  n. 周长
# 请计算圆的面积和周长，并将计算结果输出
print("圆的直径是:%.2f" % a)
print("面积：%.2f" % area)
print("周长：%.2f" % perimeter)


